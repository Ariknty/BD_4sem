select top 1
(select avg(Стоимость_минуты) from Реклама where Вид_рекламы like 'Благотворительная') [Благотворительная],
(select avg(Стоимость_минуты) from Реклама where Вид_рекламы like 'Информационная') [Информационная],
(select avg(Стоимость_минуты) from Реклама where Вид_рекламы like 'Развлекательная') [Развлекательная]
from Реклама

